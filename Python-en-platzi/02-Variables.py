#¿Qué es un identificador?
"""
El identificador no puede comenzar con un número y debe estar en minúsculas. Las palabras dentro del mismo se separan con guión bajo.
"""

numero = 3;

print(numero);

numero1 = 5;
numero2 = 6;

print(numero1, numero2);
print(numero1 + numero2);

numero1 = 12;

print(numero1);
print(numero1 + numero2);
print(numero1 - numero2);

numero_resultado = numero1 + numero2;
#Para poner dos palabras se separan por un guion bajo (_)
print(numero_resultado);